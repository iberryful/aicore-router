// ── Proxy status ─────────────────────────────────────────────────────

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum ProxyStatus {
    Starting,
    Ready,
    Active,
    Error,
}

impl ProxyStatus {
    fn icon(&self) -> &'static str {
        match self {
            Self::Starting => "\u{23f3}", // ⏳
            Self::Ready => "\u{1f7e2}",   // 🟢
            Self::Active => "\u{26a1}",   // ⚡
            Self::Error => "\u{1f534}",   // 🔴
        }
    }

    fn label(&self) -> &'static str {
        match self {
            Self::Starting => "Starting...",
            Self::Ready => "Ready",
            Self::Active => "Forwarding",
            Self::Error => "Error",
        }
    }

    fn color(&self) -> Color {
        match self {
            Self::Starting => theme::WARNING,
            Self::Ready => theme::SUCCESS,
            Self::Active => theme::WARNING,
            Self::Error => theme::ERROR,
        }
    }
}

