// ── Layout constants ────────────────────────────────────────────────
/// Horizontal margin (columns) applied to stats, logs, and footer sections.
const HORIZONTAL_MARGIN: u16 = 2;
/// Total horizontal space consumed by log widget chrome (margins + borders + padding).
const LOG_HORIZONTAL_CHROME: u16 = HORIZONTAL_MARGIN * 2 + 2 + 2; // margins + borders + padding
/// Total vertical space consumed by non-log sections (header + stats + spacer + footer + log borders + padding).
const FIXED_VERTICAL_ROWS: u16 = 13;

/// Color for quota percentage: green (0-59%), yellow (60-84%), red (85-100%).
fn quota_color(percentage: f64) -> Color {
    if percentage >= 85.0 {
        theme::ERROR
    } else if percentage >= 60.0 {
        theme::WARNING
    } else {
        theme::SUCCESS
    }
}

